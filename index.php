      <p>El programa de Tecnología en Telemática se ocupa de trabajar el campo del conocimiento surgido de la integración de las tecnologías de la comunicación y la informática, propendiendo por el rápido desarrollo de dichas tecnologías.

Mediante la Resolución 3264 del 14 de marzo de 2014, expedida por el Ministerio de Educación Nacional y registrada en el Sistema Nacional de Información de la Educación Superior (SNIES), se le otorgó el Registro Calificado a este programa de la Universidad del Cauca.</p>

<p> De igual forma, el 17 de julio de 2020 a través de la Resolución 013233, el Ministerio de Educación Nacional otorgó la Acreditación de Alta Calidad al Programa por un término de seis años y renovó el Registro Calificado para un periodo de siete años.</p>
